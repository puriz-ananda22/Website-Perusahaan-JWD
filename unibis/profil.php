        Sejak 1972, Unibis telah menerapkan 3 permata dari prinsip bisnisnya yang sederhana: Nilai, Harga, dan Kualitas. Melalui tangan orang-orang yang sangat kompeten yang dilengkapi dengan teknologi modern terbaik saat ini, hanya bahan-bahan terbaik yang dipilih dengan cermat yang ditawarkan oleh Ibu Alam untuk membuatnya menjadi produksinya. Ini telah menghasilkan produksi global produk-produk berkualitas terkemuka di pasar. Unibis terus merefleksikan komitmen “Nilai, Harga dan Kualitas” dengan mendirikan fasilitas laboratorium dan kontrol kebersihan untuk mencapai standar produk tertinggi.
